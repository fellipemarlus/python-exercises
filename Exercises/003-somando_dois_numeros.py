# Crie um programa que leia dois números e mostre a soma entre eles.

n1 = int(input("Digite o primeiro número para ser calculado: "))
n2 = int(input("Digite o segundo número para ser calculado: "))
soma = n1 + n2
print(f'O resultado da soma entre os números {n1} e {n2}, é {soma}.')

# inclui outras operações básicas
subtracao = n1 - n2
print(f'O resultado da subtração entre os números {n1} e {n2}, é {subtracao}.')

divisao = float(n1 / n2)
print(f'O resultado da divisão entre os números {n1} e {n2}, é {divisao}.')

multiplicacao = n1 * n2
print(f'O resultado da multiplicação entre os números {n1} e {n2}, é {multiplicacao}.')
